function pai(arr, firstPie, secondPie) {
    const start = arr.indexOf(firstPie);
    const end = arr.indexOf(secondPie);
    return arr.slice(start, end + 1);
}
pai(['Pumpkin Pie',
    'Key Lime Pie',
    'Cherry Pie',
    'Lemon Meringue Pie',
    'Sugar Cream Pie'],
    'Key Lime Pie',
    'Lemon Meringue Pie'
);
